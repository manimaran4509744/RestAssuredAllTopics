package org.ConstructComplexPojo2;

public class CompanyPfDetails {
private String pfName;
private String pfCity;
private int pfYear;
private int noOfEmployees;
public String getPfName() {
	return pfName;
}
public void setPfName(String pfName) {
	this.pfName = pfName;
}
public String getPfCity() {
	return pfCity;
}
public void setPfCity(String pfCity) {
	this.pfCity = pfCity;
}
public int getPfYear() {
	return pfYear;
}
public void setPfYear(int i) {
	this.pfYear = i;
}
public int getNoOfEmployees() {
	return noOfEmployees;
}
public void setNoOfEmployees(int i) {
	this.noOfEmployees = i;
}

	
	
}
